char	*ft_strcat(char *dest, char *src)
{
	int	c_1;
	int	c_2;

	c_1 = 0;
	c_2 = 0;
	while (dest[c_1] != '\0')
	{
		c_1++;
	}
	while (src[c_2] != '\0')
	{
		dest[c_1] = src[c_2];
		c_1++;
		c_2++;
	}
	dest[c_1] = '\0';
	return (dest);
}
